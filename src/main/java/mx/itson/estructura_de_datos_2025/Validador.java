    private void btnValidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidadActionPerformed
        //titulo
        lbltitulo.setForeground(new java.awt.Color(153, 0, 225));  
        //boton
        btnValidad.setBackground(new java.awt.Color(153, 0, 225));
        // el print de abajo
        lblResultado.setForeground(new java.awt.Color(0, 0, 0));
            String correo = tfCorreo.getText();

            if(
                    (correo.contains("gmail") || 
                    correo.contains("outlook") ||
                    correo.contains("hotmail") || 
                    correo.contains("icloud")) && 
                    correo.contains("@") && 
                    correo.endsWith(".com") && 
                    !correo.startsWith("@")){
                pamell.setBackground(new java.awt.Color(60,217,0));
                lblResultado.setText("CORREO VALIDO");
            }else{
               pamell.setBackground(new java.awt.Color(153,0,0));
               lblResultado.setText("CORREO INVALIDO");
            }
            tfCorreo.setText("");
    }//GEN-LAST:event_btnValidadActionPerformed

